#interactive coding challenge

#Allow user to enter a 2 digit value
user_number = input("Please enter a two digit number: ")
#Convert the 2 digit value to int and add them
num_1 = int(user_number[0])
num_2 = int(user_number[1])
adding_process = num_1+num_2
#print the added value
print(f"The summation of the two numbers is: {adding_process}")